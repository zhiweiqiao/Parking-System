package com.DAO;

/**
 * @author 乔至威
 * 本类为汽车类，定义了汽车类的操作方法
 */

public class Cars
{
	private String No;
	private String Type;
	
	public String getNo()
	{
		return this.No;
	}
	public void setNo(String No)
	{
		this.No = No;
	}
	
	public String getType()
	{
		return this.Type;
	}
	public void setType(String Type)
	{
		this.Type = Type;
	}

}
